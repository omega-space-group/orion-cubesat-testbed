void FDCAN_Tx(){
	CAN1.TxHeader.Identifier = 0x300;
	CAN1.TxHeader.DataLength = FDCAN_DLC_BYTES_6;
	CAN1.TxData[0] = 1;
	CAN1.TxData[0] |= 1<<1;
	CAN1.TxData[0] |= 1<<2;
	CAN1.TxData[0] |= 1<<3;
	CAN1.TxData[0] |= 1<<4;
	CAN1.TxData[0] |= 1<<5;
	CAN1.TxData[0] |= 1<<6;

	CAN1.TxData[1] = 1;
	CAN1.TxData[2] = 1;
	CAN1.TxData[4] = 1;
	CAN1.TxData[5] = 1;

	if (HAL_FDCAN_GetTxFifoFreeLevel(&hfdcan1) > 0)
	{
		HAL_FDCAN_AddMessageToTxFifoQ(&hfdcan1,&CAN1.TxHeader,CAN1.TxData);
	}
	else
	{
		uint32_t txFifoRequest = HAL_FDCAN_GetLatestTxFifoQRequestBuffer(&hfdcan1);
		if (HAL_FDCAN_IsTxBufferMessagePending(&hfdcan1, txFifoRequest)) {
			HAL_FDCAN_AbortTxRequest(&hfdcan1, txFifoRequest);
		}
	}
}

